# Ключевые слова, указывающие на «продажный» контекст (стоит допродавать)
_SALES_TRIGGERS = re.compile(
    r'(цен|скольк|куп|закаж|заброни|доставк|кормл|пород|бройлер|муллард|индюк|цыпл|утят|гус|несуш|яйц|вывод)',
    re.IGNORECASE
)

# Контексты, где добавлять CTA НЕЛЬЗЯ (информационные/сервисные)
_NO_CTA_TRIGGERS = re.compile(
    r'(где.*наход|адрес|работа|график|привет|здравствуй|спасиб|пока|до свидан)',
    re.IGNORECASE
)


def resolve_breed_synonyms(query: str) -> str:
    """Подставляет официальные названия пород вместо народных синонимов.
    Например: 'хочу пекинку' → добавит '(Стар-53, Черри Велли, Агидель)'
    """
    q_lower = query.lower()
    additions = []
    for synonym, breeds in BREED_SYNONYMS.items():
        if synonym in q_lower:
            additions.append(f"[Синонимы пород: {synonym} → {', '.join(breeds)}]")
    if additions:
        return query + "\n" + "\n".join(additions)
    return query


def apply_sales_layer(query: str, response: str) -> str:
    """Добавляет слой продаж к ответу модели — но ТОЛЬКО когда это уместно"""
    
    # Не добавляем CTA к коротким сервисным ответам
    if _NO_CTA_TRIGGERS.search(query):
        return response

    # Добавляем только если контекст — продажный
    if _SALES_TRIGGERS.search(query) or _SALES_TRIGGERS.search(response):
        # Выбираем ОДИН подходящий элемент, не оба
        if random.random() < 0.6:
            # 60% — мягкое закрытие
            closing = random.choice(SALES_STRATEGY["soft_close_tactics"])
            return f"{response}\n\n👉 {closing}"
        else:
            # 40% — экспертный крючок
            hook = random.choice(SALES_STRATEGY["expert_hooks"])
            return f"{response}\n\n💡 {hook}"

    # Нейтральный контекст — просто возвращаем как есть
    return response
